// Utilize if/else para fazer um programa que retorne o 
// maior de três números. Defina, no começo do programa, 
// três constantes com os valores que serão comparados.

const a = 10
const b = 15
const c = 20

if (a>b && a>c){
    console.log ('Maior numero: A')
}else if (b>a && b>c){
    console.log ('Maior numero: B')
}else{
    console.log ('Maior numero: C')
}