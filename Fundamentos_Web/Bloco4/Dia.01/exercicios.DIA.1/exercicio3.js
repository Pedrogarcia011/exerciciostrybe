//Utilize if/else para fazer um programa que, dado um 
//valor recebido como parâmetro, retorne “positive” se 
//esse valor for positivo, “negative” se for negativo, e 
//caso não seja nem positivo e nem negativo retorne “zero”.

const a = 11
const b = 12

if (b>a){
    console.log('Positive')
} else if (b<a){
    console.log('Negative')
} else {
    console.log('zero')
}